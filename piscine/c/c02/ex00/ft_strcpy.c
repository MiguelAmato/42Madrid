char	*ft_strcpy(char *dest, char *src)
{
	int	cont;

	cont = 0;
	while (*src != '\0')
	{
		*dest = *src;
		++dest;
		++src;
		++cont;
	}
	*dest = '\0';
	dest -= cont;
	return (dest);
}
